 <p>
  Crypto is not like traditional banking, where you can call customer support to reverse a transaction or recover lost funds, so it is crucial to understand how to manage your assets properly and protect yourself from losses.
  <br>
  <br>
  Crypto assets are primarily traded on two types of platforms: centralized platforms and decentralized platforms.
  <br>
  Centralized platforms require you to deposit your assets into their custody while they handle and process your transactions, whereas decentralized platforms allow you to retain full control of your assets in your wallet while making transactions.
  
  <br>
  <br>

  In this lesson, we will focus on decentralized platforms, and by the end of this session, you should have a solid understanding of decentralized exchanges and how to use them.
  <br>
  <br>
