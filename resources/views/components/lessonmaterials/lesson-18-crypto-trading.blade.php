     <ol class="list-decimal indent-2 pl-2 space-y-1 list-inside">
        <li><strong>They determine what you can trade</strong>
            <p>Exchanges do not let you trade any coin for any other coin directly. You can only trade coins that have a <strong>direct pair</strong> listed.</p>
