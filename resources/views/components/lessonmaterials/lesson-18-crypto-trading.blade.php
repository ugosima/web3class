      <h2>Trading/Currency pairs</h2>

    <p> Trading pairs are combinations of two different currencies that can be traded for each other on an exchange. They represent the price relationship between two assets, typically one base currency and one quote currency, symbolized as <b>BASE/QUOTE</b>. For example, if you see the trading pair <strong>BTC/USDT</strong>, it means you are buying or selling <strong>Bitcoin (BTC)</strong> using <b>Tether (USDT)</b>.</p>
    <br>

    <p class="pl-5">
      In (<b>ETH/BTC</b>) :
      <ol  class="pl-5">
          <li><strong>ETH</strong> is the <strong>base currency</strong>, i.e. the one you want to buy or sell.</li>
          <li><strong>BTC</strong> is the <strong>quote currency</strong>, i.e. the one you use to pay or receive.</li>
      </ol>
      <span class="pl-5">
        If <b>ETH/BTC</b> = 0.07, it means that 1 <b>ETH</b> = <b>0.07 BTC</b>, so you need 0.07 <b>BTC</b> to buy 1 <b>ETH</b>, and if you sell 1 <b>ETH</b>, you will receive 0.07 <b>BTC</b>.
      </span>
    </p>
    <br>


    
          <div class="quickbox">
     <h2 class="qbheading"><b class="ideabulb">&#128161; </b> <b class="qbheading"> IMPORTANCE OF PAIR KNOWLEDGE  </b> </h2>
