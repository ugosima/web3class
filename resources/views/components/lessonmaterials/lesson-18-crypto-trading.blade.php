        <li><strong>They affect your route</strong>
            <p>Say you have USDT and you want to buy a small token like <b> $XYZ </b>, but the exchange lists <b>$XYZ/BNB</b>. You will need to first buy <b>BNB</b> with <b>USDT</b>, then buy <b>$XYZ</b> with <b>BNB</b>.
                <br>
                This process is called a <strong>trading path</strong>.</p>
        </li>
        <li><strong>They define the price you see</strong>
            <p>You will see coin prices based on the quote currency.</p>
            <ul>
                <li> <b> BTC/USDT</b> = $29,000; This means 1 <b>BTC</b> costs 29,000 <b>USDT</b>.</li>
                <li><b>ETH/BTC</b> = 0.07; This means 1 <b>ETH</b> costs 0.07 <b>BTC</b>.</li>
            </ul>
        </li>
    </ol>
    </p>
