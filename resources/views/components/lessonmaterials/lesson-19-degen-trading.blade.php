        <div class="space-y-8">

            <div class=" border border-gray-700 rounded-2xl p-6 space-y-4">

                <h3 class="text-2xl font-semibold text-yellow-400">
                    Market Capitalization
                </h3>

                <p class="pl-4">
                    Market capitalization, often called market cap, refers to the total value of a token in circulation.
                </p>

                <div class="     rounded-xl p-4 overflow-x-auto  bg-gray-800 text-green-400 font-mono text-sm">
                    Market Cap = Token Price × Circulating Supply
                </div>

                <p class="pl-4">
                    Low market-cap tokens are attractive to degen traders because small amounts of money can move price dramatically.
                </p>

            </div>



            <div class=" border border-gray-700 rounded-2xl p-6 space-y-4">

                <h3 class="text-2xl font-semibold text-yellow-400">
                    Liquidity
                </h3>

                <p class="pl-4">
                    Liquidity refers to how much capital is available for buying and selling a token.
                </p>

                <p class="pl-4">
                    Low liquidity creates:
                </p>

                <ul class="list-disc pl-12 space-y-2">
                    <li>violent price swings</li>
                    <li>poor trade execution</li>
                    <li>easier price manipulation</li>
                    <li>larger slippage</li>
                </ul>

            </div>



            <div class=" border border-gray-700 rounded-2xl p-6 space-y-4">

                <h3 class="text-2xl font-semibold text-yellow-400">
                    Slippage
                </h3>

                <p class="pl-4">
                    Slippage occurs when a trade executes at a different price than expected because price movement happens too quickly.
                </p>

                <div class=" rounded-xl p-4 text-sm">
                    <p>Expected Buy Price = $1.00</p>
                    <p>Actual Execution = $1.15</p>
