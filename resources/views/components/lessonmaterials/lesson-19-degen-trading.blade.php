    <section class="mt-12 space-y-6">

        <h2 class="text-3xl font-bold    border-l-4 border-green-500 pl-4">
            Emotional discipline and trade management
        </h2>

        <div class="space-y-4">

            <p class="pl-4">
                Most beginners fail because of emotional decisions rather than lack of technical knowledge.
            </p>

            <p class="pl-4">
                Common emotional mistakes include:
            </p>

            <ol class="list-disc list-inside indent-4 pl-12 space-y-2  ">
                <li>Panic selling : Selling off your tokens for lesser because the prices are dipping, and you are scared that it will go lower.</li>
                <li>Revenge trading : After incurring some losses, an inexperienced trader gets emotional and plunges himself into the market in a bid to recover their capital, thereby incurring more losses.</li>
                <li>FOMO buying : Buying a token because you are scared of missing out instead of properly investigating the token.</li>
                <li>Greed during pumps : This happens when a trader does not take profit because he believes that the token will go higher, which might not be the case.</li>
                <li>Refusing to cut losses : This happens when a trader does not sell off because he believes that the token should recover and at least get to his entry price.</li>
            </ol>

            <p class="pl-4">
                Professional traders focus on probability, consistency, discipline, and survival rather than emotional reactions.
            </p>

        </div>

