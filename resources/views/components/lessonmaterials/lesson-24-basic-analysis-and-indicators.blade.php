
<div class="space-y-8 leading-8">

    <section class="space-y-4">
        <h1 class="text-3xl font-bold   ">
            Basic Trading Analysis and Technical Indicators
        </h1>

        <p>
            Trading in cryptocurrency is the process of buying and selling digital assets such as Bitcoin and Ethereum in an attempt to make profit from price movements.
        </p>

        <p>
            However, successful trading is not based on guessing or emotions. Professional traders study market behavior before making decisions. This study is called <strong>trading analysis</strong>.
        </p>

        <p>
            Trading analysis helps traders understand:
        </p>

        <ol class="list-disc pl-8 space-y-2">
            <li>Market direction</li>
            <li>Possible entry points</li>
            <li>Possible exit points</li>
            <li>Market strength or weakness</li>
            <li>Risk levels</li>
        </ol>

        <p>
            One of the most important parts of trading analysis is the use of <strong>technical indicators</strong>.
        </p>
    </section>

    <section class="space-y-4">
        <h2 class="text-2xl font-semibold   ">
            What is Technical Analysis?
        </h2>

        <p>
            Technical analysis is the study of price charts, market patterns, trading volume, and indicators to predict possible future price movement.
        </p>

        <p>
            Technical analysis is based on three major ideas:
        </p>

        <ol class="list-[lower-roman] pl-8 space-y-2">
            <li>The market reflects all information</li>
            <li>Prices move in trends</li>
            <li>History tends to repeat itself</li>
        </ol>

        <p>
            Instead of focusing mainly on news or project details, technical analysts focus on chart behavior.
        </p>
    </section>

    <section class="space-y-4">
        <h2 class="text-2xl font-semibold   ">
            Understanding Market Trends
        </h2>

        <p>
            Before using indicators, traders must first understand trends.
        </p>

        <p>
            A trend is the general direction of the market. A trader is expected to respect the general direction of the market.
        </p>

        <p>
            There are three major market trends:
        </p>

        <div class="space-y-6">

            <div class="  border border-gray-800 rounded-2xl p-6 space-y-3">
                <h3 class="text-xl font-semibold text-green-400">
                    1. Uptrend
                </h3>

                <p>
                    An uptrend happens when price keeps making:
                </p>

                <ul class="list-disc pl-8 space-y-2">
                    <li>Higher highs</li>
                    <li>Higher lows</li>
                </ul>

                <p>
                    This means buyers are stronger than sellers.
                </p>

                <p>
                    Characteristics:
                </p>

                <ol class="list-disc pl-8 space-y-2">
                    <li>Bullish market sentiment</li>
                    <li>Rising prices</li>
                    <li>Strong buying pressure</li>
                </ol>
            </div>

            <div class="  border border-gray-800 rounded-2xl p-6 space-y-3">
                <h3 class="text-xl font-semibold text-red-400">
                    2. Downtrend
                </h3>

                <p>
                    A downtrend happens when price keeps making lower highs and lower lows.
                    This means sellers are stronger than buyers.
                </p>

                <p>
                    Characteristics:
                </p>

                <ol class="list-disc pl-8 space-y-2">
                    <li>Bearish market sentiment</li>
                    <li>Falling prices</li>
                    <li>Strong selling pressure</li>
                </ol>
            </div>

            <div class="  border border-gray-800 rounded-2xl p-6 space-y-3">
                <h3 class="text-xl font-semibold text-yellow-400">
                    3. Sideways Trend
                </h3>

                <p>
                    A sideways market happens when price moves within a range without clear direction.
                </p>

                <p>
                    Characteristics:
                </p>

                <ol class="list-disc pl-8 space-y-2">
                    <li>Uncertainty</li>
                    <li>Low momentum</li>
                    <li>Price bouncing between support and resistance</li>
                </ol>
            </div>

        </div>
    </section>

    <section class="space-y-4">
        <h2 class="text-2xl font-semibold   ">
            What are Technical Indicators?
        </h2>

        <p>
            Technical indicators are mathematical tools built from price, volume, or market activity.
        </p>

        <p>
            They help traders:
        </p>

        <ol class="list-disc pl-8 space-y-2">
            <li>Identify trends</li>
            <li>Measure momentum</li>
            <li>Detect overbought or oversold conditions</li>
            <li>Confirm trading signals</li>
        </ol>
    </section>

    <section class="space-y-4">
        <h2 class="text-2xl font-semibold   ">
          <b>1.</b>  Support and Resistance
        </h2>

        <div class="space-y-6">

            <div class="dark:bg-blue-50 border dark:border-blue-200 rounded-2xl p-6 space-y-3 bg-blue-950/30 border-blue-800">
    <h3 class="text-xl font-semibold dark:text-blue-700 text-blue-300">
       <b>i.</b> Support
    </h3>

                <p>
                    Support is a price level where buyers usually enter the market and prevent price from falling further.
                </p>

                <p>
                    It acts like a floor beneath price.
                </p>
            </div>

            <div class="dark:bg-purple-50 border dark:border-purple-200 rounded-2xl p-6 space-y-3 bg-purple-950/30 border-purple-800">
    <h3 class="text-xl font-semibold text-purple-700 dark:text-purple-500">
       <b>ii.</b> Resistance
    </h3>

                <p>
                    Resistance is a price level where sellers usually enter the market and stop price from rising higher.
                </p>

                <p>
                    It acts like a ceiling above price.
                </p>
            </div>

        </div>
    </section>

    <section class="space-y-4">
        <br><br>
        <h2 class="text-2xl font-semibold   ">
           <b>2.</b> Candlestick patterns
        </h2>

        <p>
            As discussed in the previous lesson, candlesticks are visual representations of price movement during a specific time period. A candle with a specific shape, or a series of candlesticks when seen on a candlestick chart forms a pattern that historically can be used to analyze the market.
        </p>
        <br>
        <h2 class="text-2xl font-semibold   ">
            Popular candlestick patterns
        </h2>

        <div class="space-y-6">

            <div class="  border border-gray-800 rounded-2xl p-6 space-y-3">
                <h3 class="text-xl font-semibold   ">
                  <b>i.</b>  Doji
                </h3>

                <p>
                    A doji candle shows indecision between buyers and sellers.
                </p>

                <p>
                    It often appears before trend reversals or strong breakouts.
                </p>
            </div>

            <div class="  border border-gray-800 rounded-2xl p-6 space-y-3">
                <h3 class="text-xl font-semibold   ">
                  <b>ii.</b>  Hammer
                </h3>

                <p>
                    A hammer candle usually appears after a downtrend.
                </p>

                <p>
                    It may signal buyer recovery and potential bullish reversal.
                </p>
            </div>

            <div class="  border border-gray-800 rounded-2xl p-6 space-y-3">
                <h3 class="text-xl font-semibold   ">
                  <b>iii.</b>  Engulfing Candle
                </h3>

                <p>
                    An engulfing pattern happens when one candle fully covers the previous candle.
                </p>

                <p>
                    This often signals strong momentum.
                </p>
            </div>

        </div>
    </section>

    

    <section class="space-y-6">

        <div class="bg-orange-950/30 dark:bg-orange-50  border border-orange-800 dark:border-orange-200 rounded-2xl p-6 space-y-4">
            <h2 class="text-2xl font-semibold text-orange-300 dark:text-orange-700">
               <b>3.</b> Moving Averages
            </h2>

            <p>
                Moving averages smooth out price movement and help traders identify trend direction.
            </p>

            <div class="space-y-4">
                <div>
                    <h3 class="text-lg font-semibold   ">
                       <b>i.</b> Simple Moving Average (SMA)
                    </h3>

                    <p>
                        The SMA calculates the average price over a specific number of periods.
                    </p>
                </div>

                <div>
                    <h3 class="text-lg font-semibold   ">
                       <b>ii.</b> Exponential Moving Average (EMA)
                    </h3>

                    <p>
                        The EMA gives more weight to recent prices, making it react faster to market changes.
                    </p>
                </div>
            </div>
        </div>

        <div class="bg-cyan-950/30 dark:bg-cyan-50 border border-cyan-800 dark:border-cyan-200 rounded-2xl p-6 space-y-4">
            <h2 class="text-2xl font-semibold text-cyan-300 dark:text-cyan-700">
              <b>4.</b>  Relative Strength Index (RSI)
            </h2>

            <p>
                The RSI measures market momentum and speed of price movement.
            </p>

            <p>
                It ranges from 0 to 100.
            </p>

            <ul class="list-disc pl-8 space-y-2">
                <li>Above 70 may indicate overbought conditions</li>
                <li>Below 30 may indicate oversold conditions</li>
            </ul>
        </div>

        <div class="bg-pink-950/30 dark:bg-pink-50 border border-pink-800 dark:border-pink-200 rounded-2xl p-6 space-y-4">
            <h2 class="text-2xl font-semibold text-pink-300 dark:text-pink-700">
                <b>5.</b>MACD
            </h2>

            <p>
                The MACD is a trend-following momentum indicator.
            </p>

            <p>
                It helps traders identify:
            </p>

            <ol class="list-disc pl-8 space-y-2">
                <li>Trend strength</li>
                <li>Trend direction</li>
                <li>Momentum changes</li>
            </ol>
        </div>

        <div class="bg-indigo-950/30 dark:bg-indigo-50 border border-indigo-800 dark:border-indigo-200 rounded-2xl p-6 space-y-4">
            <h2 class="text-2xl font-semibold text-indigo-300 dark:text-indigo-700">
              <b>6.</b>  Bollinger Bands
            </h2>

            <p>
                Bollinger Bands measure market volatility.
            </p>

            <ul class="list-disc pl-8 space-y-2">
                <li>Wide bands indicate high volatility</li>
                <li>Tight bands may signal an upcoming breakout</li>
            </ul>
        </div>

    </section>

    <section class="space-y-4">
        <h2 class="text-2xl font-semibold   ">
         <b>7.</b>   Volume Analysis
        </h2>

        <p>
            Volume shows the amount of buying and selling activity in the market.
        </p>

        <p>
            High volume often confirms strong trends and breakouts, while low volume may indicate weak momentum.
        </p>
    </section>

    <section class="space-y-4">
        <h2 class="text-2xl font-semibold   ">
            Combining Indicators
        </h2>

        <p>
            As a professional trader, you should always combine multiple indicators for confirmation.
        </p>

        <div class="  border border-gray-800 rounded-2xl p-6">
            <ul class="list-disc pl-8 space-y-2">
                <li>Trend confirmation using EMA</li>
                <li>Momentum confirmation using RSI</li>
                <li>Breakout confirmation using volume</li>
            </ul>
        </div>
    </section>

    <section class="space-y-6">
        <h2 class="text-2xl font-semibold   ">
            Risk Management in Technical Trading
        </h2>

        <div class="grid md:grid-cols-3 gap-6">

            <div class="bg-red-950/30 dark:bg-red-50 border border-red-800 dark:border-red-200 rounded-2xl p-6 space-y-3">
                <h3 class="text-xl font-semibold text-red-300 dark:text-red-400">
                    Stop Loss
                </h3>

                <p>
                    A stop loss automatically closes a trade at a specific loss level to protect trading capital.
                </p>
            </div>

            <div class="bg-green-950/30 dark:bg-green-50 border border-green-800 dark:border-green-200 rounded-2xl p-6 space-y-3">
                <h3 class="text-xl font-semibold text-green-300 dark:text-green-400">
                    Take Profit
                </h3>

                <p>
                    A take profit automatically closes a trade after reaching a target profit level.
                </p>
            </div>

            <div class="bg-yellow-950/30 dark:bg-yellow-50 border border-yellow-800 dark:border-yellow-200 rounded-2xl p-6 space-y-3">
                <h3 class="text-xl font-semibold text-yellow-300 dark:text-yellow-600">
                    Position Sizing
                </h3>

                <p>
                    Professional traders usually risk only a small percentage of their account balance per trade.
                </p>
            </div>

        </div>
    </section>

    <section class="space-y-4">
        <h2 class="text-2xl font-semibold   ">
            Common Beginner Mistakes
        </h2>

        <ol class="list-disc pl-8 space-y-3">
            <li>Relying on one indicator only</li>
            <li>Ignoring the market trend</li>
            <li>Emotional trading</li>
            <li>Overtrading</li>
        </ol>
    </section>

