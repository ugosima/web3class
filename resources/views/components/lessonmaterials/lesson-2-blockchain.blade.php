     <h2 class="qbheading"><b class="ideabulb">&#128161; </b> <b class="qbheading"> ANALOGY: HOW BLOCKCHAIN WORKS </b> </h2>
    <br>

  <p>Imagine a classroom where every student has a notebook. This notebook is used to record every time someone borrows or returns a book from the class library.</p>
  <br>

  <h3>These Distinct Steps Follow:</h3>

  <ol class="pl-5 indent-5">
    <li><strong>A Transaction Happens:</strong> John borrows a book titled "Science Today".</li>
    <li><strong>Everyone Sees It:</strong> Instead of one person writing it down, all students copy the same transaction into their notebooks: <br>
      <em>"John borrowed 'Science Today' at 10:00 AM"</em>
    </li>
    <li><strong>Information is Locked In:</strong> Once all students write it down, that page in the notebook is considered complete and can't be changed.</li>
    <li><strong>The Chain Continues:</strong> The next transaction (e.g., Mary returns "Math Basics") is recorded on a new page, following the previous one.</li>
  </ol>

  <p class="pl-3">This way, the class keeps a tamper-proof, shared history of every library transaction. If someone tries to cheat and change their notebook, it won't match others—and everyone will know!</p>
  <br>

  <h3> How This Relates to Blockchain:</h3>

  <ol class="pl-5 indent-5">
    <li><strong>The notebook</strong> represents the blockchain.</li>
    <li><strong>The students</strong> are like the network nodes (computers).</li>
    <li><strong>The recorded entries</strong> are the blocks.</li>
    <li><strong>The linking of pages</strong> is the chain of blocks.</li>
    <li><strong>Consensus among students</strong> ensures no one can fake or alter history.</li>
  </ol>
<br>
  <p><strong>Summary:</strong> Just like the classroom notebook system, blockchain creates a shared, secure, and permanent record of transactions—open to all, but impossible to alter without agreement from everyone involved.</p>

