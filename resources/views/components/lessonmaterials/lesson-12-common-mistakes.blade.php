        Understanding wallets, private keys, scams, and transaction risks is essential before interacting with cryptocurrencies or decentralized applications.
    </p>
    <br>

    <p>
        In Web3, ownership is controlled through cryptographic keys instead of usernames and passwords stored on company servers. 
    </p>

    <ol class="list-[lower-roman] list-inside space-y-2  indent-4">
        <li>Wallets replace bank accounts</li>
        <li>Private keys replace passwords</li>
        <li>Users control their own assets</li>
        <li>Transactions cannot easily be reversed</li>
    </ol>

    <p>
        This system removes intermediaries, but it also means users must protect their own accounts and assets.
    </p>

