    <h2>Understanding common Web3 scams</h2>

    <p>
        Because blockchain transactions are irreversible, scammers actively target inexperienced users.
        Most attacks focus on stealing wallet access or tricking users into approving malicious transactions. 
        <br>
        Let's look at some of the most common scams in the Web3 ecosystem and how to avoid them.
    </p>
    <br>




    <div class="pl-6"> 

    <h3>Phishing Attacks</h3>

    <p>
       Phishing is a common tactic used by scammers to trick users into revealing sensitive information. It involves creating fake versions of popular websites, applications, or messages designed to steal sensitive information. These fake platforms often mimic legitimate services to trick users into revealing private keys, seed phrases, or wallet credentials. Common phishing scams include:
    </p>
    <br>

    <ol class="list-[lower-roman] list-inside space-y-2  indent-4">
        <li>Fake exchange login pages</li>
        <li>Fake wallet applications</li>
        <li>Fake customer support accounts</li>
        <li>Fake airdrop websites</li>
    </ol>
    <br>
    
<div class="quickbox">
     <h2 class="qbheading"><b class="ideabulb">&#128161; </b> <b class="qbheading"> PHISHING SCAM </b> </h2>
      <br>
    <p>
     <i>Typical example : </i> "<b>https://metamask.io</b>" is the official website of MetaMask, but a phishing site might look like "<b>https://metamask-secure.com</b>" or "<b>https://metamask-login.com</b>". These fake sites are designed to look exactly like the original, but they exist only to steal your information.
    </p>

    <p>
        Some phishing sites are designed only to collect emails and passwords, which scammers may use to target your other accounts because many users repeat passwords across platforms. Avoid reusing passwords on important platforms, and enable two-factor authentication, preferably with an authenticator app, whenever possible.
    </p>
      <br>
      <p>
          Always verify URLs, avoid clicking on suspicious links, and never share your private keys or seed phrases with anyone.
      </p>
      <br>
      <p>If you receive an email claiming to be from your wallet provider and asking you to "verify your account" by entering your seed phrase, it is most likely a phishing attempt. Legitimate services will never ask for your seed phrase.</p>
</div>
<br><br>





    <h3>Fake giveaways</h3>

    <blockquote>
        "Send 1 ETH and receive 2 ETH back."
    </blockquote>

    <p>
        Giveaway scams promise unrealistic rewards in exchange for advance payments. You have to understand that legitimate giveaways do not require users to send cryptocurrency first, so any giveaway asking for an upfront payment is a scam. Always verify official project channels for legitimate promotions and be skeptical of offers that sound too good to be true.
    </p>

    <br><br>

    <h3>Rug Pulls</h3>

    <p>
        A rug pull occurs when developers abandon a project after collecting investor funds, often by creating a token, promoting it, and then selling off their holdings, causing the token's value to plummet. Rug pulls are common in DeFi and NFT projects with anonymous teams and little transparency. Always research a project's team, utility, and security audits before investing to reduce the risk of falling victim to a rug pull.
    </p>

    <p>
        Warning signs include:
    </p>

    <ol class="list-[lower-roman] list-inside space-y-2  indent-4">
        <li>Anonymous development teams</li>
        <li>Unrealistic promises</li>
        <li>No clear utility</li>
        <li>No security audits</li>
    </ol>

    <br><br>





    <h3>Smart contracts and transaction risks</h3>

    <p>
        Many Web3 applications require users to approve smart contracts before interacting with tokens or assets.
    </p>

    <p>
        Some approvals grant spending permission over wallet assets. Malicious contracts can abuse these permissions to drain funds.
        
    <blockquote class="pl-4">
        <b>** Never approve a transaction without understanding what it does. **</b>
    </blockquote>
    </p>


    <p>
        Always verify websites carefully before connecting wallets or signing transactions.
    </p>

    <br><br>

