  <h2>Common mistakes that beginners make (summarised)</h2>
  <ol class="list-[lower-roman] list-inside space-y-2  indent-4">
    <li>
      <b>Not backing up wallet seed phrase:</b>
      Losing your recovery or seed phrase means permanent loss of access to your wallet. Storing it digitally (e.g., screenshots or cloud notes) makes it vulnerable to hacks, so always write it down on paper as soon as possible and store it securely.
    </li>
    <li>
      <strong>Sharing private keys or seed phrases:</strong>
      Your seed phrase is like a master key. Anyone with it can steal your funds. Never share it, even if someone claims to be "Support" or "Admin," which is a common scam tactic on social media platforms like X (formerly Twitter).
    </li>
    <li>
      <strong>Falling for scams and phishing:</strong>
      Fake wallet apps, websites, or giveaway scams are common. Always verify URLs and avoid clicking links in DMs or emails.
    </li>
    <li>
      <strong>Investing without research (FOMO):</strong>
      Many beginners are in a hurry to make money, so they buy coins simply because they are trending or heavily hyped. This fear of missing out often leads to poor decision-making. You need to stay calm, research, and understand a project's utility, team, and risks.
    </li>
    <li>
      <strong>Sending funds to the wrong address or network:</strong>
      Crypto transactions are irreversible. Sending funds to the wrong address or through an incompatible network can lead to permanent loss. Changing even a single character in an address can result in lost funds.
    </li>
    <li>
      <strong>Investing more than they can afford to lose:</strong>
      Crypto markets are volatile. Never invest emotionally or with money you cannot afford to lose. A token's past performance is not a guarantee of future results, because the crypto ecosystem is constantly changing.
    </li>
    <li>
      <strong>Not understanding network fees:</strong>
      Some networks have high gas fees, so unnecessarily transferring assets from one wallet to another is an easy way to lose money. Always check the fees before making transactions, consider using networks with lower fees for smaller transactions, and avoid unnecessary transfers.
    </li>
    <li>
      <strong>Leaving funds on exchanges:</strong>
      Centralized exchanges can be hacked, and they can also freeze withdrawals. Move your funds to a secure wallet if you are not actively trading or if you plan to hold for a long time. Always use reputable exchanges and enable two-factor authentication (2FA) for added security.
    </li>
    <li>
      <strong>Ignoring security best practices:</strong>
            Not using 2FA, or using weak or reused passwords, puts your assets at risk.
    </li>
    <li>
      <strong>Not understanding taxes or legal requirements:</strong>
        In some countries and regions, crypto may be taxable. Beginners often fail to track their transactions properly, which can cause issues later.
    </li>
  </ol>

