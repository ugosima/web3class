    <h2>Account security on Centralized Exchanges</h2>

    <p>
        Centralized exchanges allow users to buy, sell, and store cryptocurrency.
        Although convenient, they introduce risks such as exchange hacks, frozen accounts, and withdrawal restrictions.
    </p>

    <h3>Basic Exchange Security</h3>

    <ol class="list-[lower-roman] list-inside space-y-2  indent-4">
        <li>Use strong passwords</li>
        <li>Enable two-factor authentication</li>
        <li>Avoid password reuse</li>
        <li>Store large holdings in personal wallets</li>
    </ol>

    <blockquote class="pl-4 indent-4">
       <span> <b>Remember :</b></span> "Not your keys, not your coins."
    </blockquote>

    <p>
        This means assets fully controlled by an exchange are ultimately controlled by the exchange itself.
    </p>
