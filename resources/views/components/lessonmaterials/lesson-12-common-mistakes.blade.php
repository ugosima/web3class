    <h2>Cryptocurrency wallets</h2>

    <p>
        As covered in the previous lesson, a cryptocurrency wallet is a tool used to access and manage blockchain assets.
        Wallets store cryptographic credentials that allow users to interact with blockchain networks.
        Wallets allow you to:
    </p>

    <ol class="list-[lower-roman] list-inside space-y-2  indent-4">
        <li>Send cryptocurrency</li>
        <li>Receive cryptocurrency</li>
        <li>Store digital assets</li>
        <li>Connect to decentralized applications</li>
        <li>Sign blockchain transactions</li>
    </ol>
<br>

    <h2>Public and Private keys in a wallet</h2>

    <h3><b>Public Key</b></h3>

    <p>
        A public key, commonly called a wallet address, is used to receive cryptocurrency.
        It can be shared publicly without compromising the wallet.
    </p>

    <pre><code>0xA1B3F6E9...</code></pre>

    <br>

    <h3><b>Private Key</b></h3>

    <p>
        The private key is the secret credential that controls the wallet.
        Anyone with access to it gains full control over the assets inside the wallet.
    </p>

    <ol class="list-[lower-roman] list-inside space-y-2  indent-4">
        <li>Never share private keys</li>
        <li>Do not store them carelessly</li>
        <li>Avoid sending them online</li>
    </ol>

    <p>
        Protecting the private key is one of the most important security responsibilities in Web3.
    </p>

    <br>




    <h2>Seed phrases and wallet recovery</h2>

    <p>
        Most wallets generate a seed phrase during setup. A seed phrase is a group of words used to recover a wallet if the device is lost or damaged. For example, a seed phrase might look like this:
    </p>

    <pre class="pl-4"><code>"apple river moon yellow crystal ..."</code></pre>

    <blockquote>
        Anyone with the seed phrase can restore the wallet and access the funds.
    </blockquote>

    <h3> <u>Seed Phrase Safety</u></h3>

    <ol class="list-[lower-roman] list-inside space-y-2  indent-4">
        <li>Store it offline.</li>
        <li>Keep it private.</li>
        <li>Never enter it on non-official websites.</li>
        <li>Avoid storing it in screenshots.</li>
    </ol>
    <br>

    <p>
        Legitimate wallet providers or support teams will never ask for a seed phrase, so anybody requesting it is 100% a scammer trying to steal your funds.
    </p>

    <br><br>

    

    <h2>Device and Transaction Safety</h2>

    <p>
        Web3 security also depends on device security. Malware can steal passwords,
        monitor activity, or replace copied wallet addresses during transactions.
    </p>
        <h3><u><b>Device security</b></u></h3>
    <ol class="list-[lower-roman] list-inside space-y-2 indent-4">
        <li>Keep software updated</li>
        <li>Avoid pirated software</li>
        <li>Use antivirus protection</li>
        <li>Verify copied wallet addresses</li>
    </ol>

    <p>
      And before sending cryptocurrency:
    </p>

    <ol class="list-[lower-roman] list-inside space-y-2 indent-4">
        <li>Double-check wallet addresses</li>
        <li>Verify the correct blockchain network</li>
        <li>Send a small test transaction first when possible</li>
    </ol>

