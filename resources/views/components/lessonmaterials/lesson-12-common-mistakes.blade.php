    <p>
        Web3 changes how people interact with money, applications, and digital ownership.
        Instead of depending on banks or centralized platforms, users directly control their assets through blockchain wallets.
    <br>
        This creates freedom, but it also introduces responsibility. In traditional systems,
        fraudulent transactions may be reversed by a bank or payment provider. In cryptocurrency,
        transactions are usually permanent. If funds are stolen or sent incorrectly,
        recovery is often impossible.
    </p>

    <p>
