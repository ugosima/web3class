  <h2>Tokenomics</h2>
  <p>
    Bitcoin's economic model is deflationary, designed to create scarcity and long-term value preservation. It operates on a fixed
    set of rules embedded in its protocol. These rules determine how new coins are created, how transactions are validated, and how incentives are distributed to network participants.
  </p>
  <p>
    The key components of Bitcoin's tokenomics include:
  </p>
  <ol class="list-[lower-roman] list-inside indent-4 pl-4 space-y-2">
    <li><strong>Maximum Supply:</strong> Bitcoin has a hardcoded maximum supply of 21 million BTC.</li>
    <li><strong>Block Time:</strong> This is the average time required for the network to validate and add a new block of transactions to the blockchain. For Bitcoin, this is approximately 10 minutes.</li>
    <li><strong>Reward Halving:</strong> The amount of BTC awarded to miners is halved at regular intervals, reducing the supply of new bitcoins entering the market. For Bitcoin, this occurs every 210,000 blocks (approximately 4 years). The current reward is 3.125 BTC.</li>
    <li><strong>Mining Incentives:</strong> Miners are rewarded through a combination of block rewards and transaction fees.</li>
    <li><strong>Deflationary Nature:</strong> Bitcoin's reduced issuance over time creates deflation. All Bitcoin is expected to be fully mined by 2140.</li>
  </ol>

