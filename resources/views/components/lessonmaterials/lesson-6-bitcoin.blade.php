<h2> Whitepaper</h2>
  <p>
    Bitcoin, the first cryptocurrency, was introduced by Satoshi Nakamoto in 2008 through a whitepaper titled
    <strong>"Bitcoin: A Peer-to-Peer Electronic Cash System."</strong> It outlined a decentralized network enabling direct
    financial transactions between parties without relying on trusted intermediaries. By solving the double-spending problem
    through a consensus mechanism called <em>Proof of Work</em>, the whitepaper laid the foundation for a trustless digital
    payment system. It practically implemented concepts like distributed ledgers (blockchains), mining, and incentivized validation that are now
    standard across the cryptocurrency ecosystem.
    <br>
    <br>

    Satoshi Nakamoto did not just build the first practical blockchain, he also saw the futuristic use of the embedded payment system, which is why he gave the title of the whitepaper as "A Peer-to-Peer Electronic Cash System.", because he saw that Bitcoin primarily would be used as a medium of exchange(Money). 
  </p>
