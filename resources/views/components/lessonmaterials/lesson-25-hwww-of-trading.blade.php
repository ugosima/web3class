    <section class="space-y-4">
        <h2 class="text-2xl font-semibold">
            3. When to buy
        </h2>

        <p>
            Timing is critical. professional entries are based on confirmation, not emotion.
        </p>

        <ol class="list-disc pl-6 space-y-2">
            <li>Buy in confirmed uptrends</li>
