            <li>Confirm entries with volume increase</li>
        </ol>

        <div class="bg-green-950/20 dark:bg-green-100 border border-green-800 dark:border-green-200 rounded-2xl p-5">
            <p class="font-semibold">
                Ideal entry condition:
            </p>
            <p>
                Price pullback in an uptrend, breakout with volume, or accumulation after consolidation.
