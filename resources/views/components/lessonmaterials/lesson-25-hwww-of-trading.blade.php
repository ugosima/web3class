    <section class="space-y-4">
        <h2 class="text-2xl font-semibold">
            1. How to buy and sell crypto
        </h2>

        <p>
            After the introductory lessons to web3, blockchain, cryptocurrency, the next set of lessons that follows are practically "How to buy and sell". 
            <br>

            To buy crypto you have to use money(Fiat). Either you perform a one-on-one deal with local exchange agents around you, or you proceed to a centralised exchange to convert your Fiat to USDT(Dollar equivalent), which you can use to purchase any cryptocurrency of your choice.
            <br>

            Of course, you want a good and trusted exchange to hold your money, so you have to look for a centralised exchange that is not restricted in your country, and make the transaction. it is usually a swift and smooth process with a debit or credit card, and also, many exchanges offer the on-ramp deposit service, so funding is hassle free.

            <br>

            After funding your exchange account, your knowledge of orders will come in handy(revisit the lesson if you need to). Place an order for the crypto of your choice on the exchange; if the order executes, you will have your crypto in your exchange wallet.
            <br>

            And to sell, you also place your sell order and you get USDT, which can be converted to Fiat from the exchange or you can use services like P2P.


        </p>
    </section>
