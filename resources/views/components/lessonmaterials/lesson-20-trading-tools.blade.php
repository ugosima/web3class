<section>
    <h2>Automated Trading Bots</h2>
    <p>Software that executes trades automatically based on pre-set strategies:</p>
    <br>
    <h3><strong>3Commas</strong></h3>
    <p>A platform for automated trading, copy trading, and portfolio management across multiple exchanges.</p>
    <br>
    <h3><strong>Pionex</strong></h3>
    <p>A built-in trading bot platform that allows grid trading and DCA (Dollar Cost Averaging) automation.</p>
