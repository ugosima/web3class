 <h2>Why is crypto accepted as money?</h2> 

    <div>

    <p>
        For a blockchain to function, it needs its validators, and for its validators to keep working, they need to be paid; This is where the concept of cryptocurrency was born, cryptocurrencies are digital currencies that are used as a system of reward for nodes (people) validating a blockchain, and as a means of payment by people that are using the blockchain; This payment algorithm is wired into the blockchain at creation and becomes part of the system forever;
    </p>
    <br>

    <p>
        There is a common mistake of trying to visualize cryptocurrencies, which is why it is important to emphasize that cryptocurrencies are not physical objects but rather recorded data that can be passed from one person to another; A simple abstract analogy is that cryptocurrencies are like entitlements recorded on the blockchain, you can not touch them, but everybody knows that you are the only person entitled to them.
    </p>
    <br>
    <p>
        Now a blockchain can be built by anybody for many reasons, however if you want to use a blockchain that you did not build, you will need to pay for the services of that blockchain, and you will need to pay in the blockchain's native cryptocurrency.
    <br>
        Here's the mental model, if you want to use a blockchain, you need to pay for it, and you can only pay for it with the blockchain's cryptocurrency, so you need to buy the cryptocurrency, and to buy the cryptocurrency, you need to use money, so you exchange your money for the cryptocurrency, and then you can use the blockchain (Reread this paragraph if needed).
    </p>
    <br>
    <p>
        As many more people use the blockchain, more people need to buy the cryptocurrency, and as more people buy the cryptocurrency, if the supply is limited, the value of the cryptocurrency increases; This is the basic meaning of utility, which today of course is the main driving force for crypto prices.
    <br>
        The properties of a cryptocurrency that determines its total supply, mode of validator reward, behaviours and how it is used, are all determined at the creation of the blockchain, and they are part of the system forever; This is why cryptocurrencies are often compared to gold, because they have a limited supply and are not controlled by any central authority, which makes them a good store of value and a hedge against inflation.
    </p>
    <br>

    <p>
        You might want to ask who and who uses these blockchains, and the answer is that there are many different types of blockchains, some are public and open to anyone, while others are private and only accessible to certain organizations or individuals; Some blockchains are used for financial transactions, while others are used for supply chain management, voting systems, digital identity, government, or even social media platforms; The use cases for blockchains are diverse because they provide a secure, transparent, and decentralized way to record and verify data, which can be applied in many different industries.
    <br>
        Even if you are not part of a company, you are a user of the blockchain every time you use cryptocurrency, and you make your payment to the validators by paying gas fees (A small fee paid to the validators for processing your transaction).
    </p>

</div>
