     <p>
        In summary, cryptocurrencies are accepted as money because they are the means of payment for using blockchains i.e They are anchored to a valueable service.
      <br>
        Like Gold and other precious metals, cryptocurrencies have value because they are scarce, secure, and useful for certain extremely valueable purposes;
    </p>
