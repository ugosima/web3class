    <h2>Common types of airdrops</h2>
    <p class="pl-3">
        Airdrops can take various forms, each with its own criteria depending on the goal the developers want to achieve. Here are some common types of airdrops you might encounter:
    </p>
    <br>
    <ol class="list-[lower-roman] pl-3 list-inside mb-6 space-y-2">
        <li><b>Standard airdrop:</b> Users receive tokens just for signing up or providing their wallet address.</li>
        <li><b>Bounty airdrop:</b> Users complete specific tasks like sharing posts, joining groups, or creating content.</li>
        <li><b>Holder airdrop:</b> Tokens are given to holders of another cryptocurrency, such as ETH or a specific token.</li>
        <li><b>Exclusive airdrop:</b> Sent to selected users, often based on previous involvement with the project.</li>
        <li><b>Retroactive airdrop:</b> Rewards users who have already interacted with a platform before a certain date.</li>
    </ol>
    <br>


    <h2>How airdrops work</h2>
    <p class="pl-3">Airdrops typically follow a structured process to ensure fair distribution and engagement. Here's how they generally work:</p>
    <ol class="list-[lower-roman] pl-3 list-inside mb-6 space-y-2">
        <li>The project announces an upcoming airdrop and defines the eligibility rules. This is usually done through its social media handles and is often promoted further by influencers.</li>
        <li>Users may need to complete tasks such as following social media accounts, joining Telegram groups, or holding specific tokens in their wallets.</li>
        <li>A snapshot may be taken on the blockchain at a specific time to record wallet balances.</li>
        <li>Eligible users may need to complete tasks or simply wait to receive the tokens.</li>
        <li>The tokens are then distributed automatically or made claimable through a link.</li>
    </ol>
    <br>
    <b>How to qualify for airdrops</b>
    <p class="pl-3">To qualify for airdrops, users typically need to meet certain criteria set by the project's developers. Some users may be left out if they do not meet those requirements. Here are some common ways to become eligible:</p>
    </p>
    <br>
    <ol class="list-disc pl-5 list-inside mb-6 space-y-2">
        <li>Interact with DeFi platforms, bridges, or testnets.</li>
        <li>Hold specific tokens or NFTs in non-custodial wallets.</li>
        <li>Participate early in a protocol's ecosystem.</li>
        <li>Stay informed through official project channels and community forums.</li>
    </ol>
    <br>

    <h2>Classes of airdrops</h2>
    <p>
        Airdrops can also be categorized into different classes based on the tasks performed. Here are some common classes of airdrops:
    </p>
    <br>
    <h4><strong>Testnets</strong></h4>
    <p class="pl-3">Testnets are networks used for testing before launching on the mainnet. Airdrops on testnets reward users who interact with the platform during its development phase.</p>
    <br>
    <h4><strong>Retroactive Airdrops</strong></h4>
    <p class="pl-3">Retroactive airdrops reward users who previously interacted with a platform, even before its official launch. This encourages early adoption and community engagement.</p>
    <br>    
    <h4><strong>Yapping</strong></h4>
    <p class="pl-3">Yapping refers to airdrops that require users to engage in social media activities, such as sharing posts, joining groups, or creating content. This helps increase the project's visibility and reach.</p>
    <br>
    <h4><Strong>DEPIN</Strong></h4>
    <p class="pl-3">DEPIN (Decentralized Physical Infrastructure Networks) airdrops are designed to reward users who contribute to the development of decentralized physical infrastructure. This can include tasks like running nodes, providing resources, or participating in governance.</p>
    <br>
    <h4><strong>Mining</strong></h4>
    <p class="pl-3">Mining airdrops are distributed to users who contribute computational power to secure the network. This is common in proof-of-work blockchains, where miners are rewarded with tokens for validating transactions and maintaining the network.</p>
    <br>



    <h2>Risks and cautions</h2>
    <ol class="list-disc pl-4 list-inside space-y-2 ">
        <li><strong>Phishing Scams:</strong> Beware of fake airdrops asking for private keys or wallet access.</li>
        <li><strong>Dusting Attacks:</strong> Some fake tokens may be sent to wallets to track or exploit users.</li>
        <li><strong>Privacy Risks:</strong> Sharing wallet addresses or linking social media can expose personal data.</li>
        <li><strong>Tax Liabilities:</strong> Airdropped tokens may be considered taxable income in some jurisdictions.</li>
    </ol>
