    <h2>Candlestick patterns and analysis</h2>

    <p>
        To efficiently trade crypto for profit, a trader has three very important decisions to make,
        these are :
        <ol class="list-[lower-roman] indent-4 marker:font-bold list-inside">
            <li>What to buy</li>
            <li>When to buy</li>
            <li>When to sell</li>
        </ol>
    </p>
    <p>
     There is no  single metric that is used to solve any of this problems, so the approach is probablistic rather than deterministic. i.e a trader watches the market and tentatively tries to determine what will happen next, if he is right, he makes profit, and if he is wrong, then he incurs losses.
     <br>
     
     Candlestick charts forms the basis of these tentative decisions a trader makes; He studies the patterns that has happened historically, checks the coins tokenomics, and studies market sentiment and trends, before deciding whether to invest his money.
     <br>

        <b>Remember: </b>Crypto is extremely volatile, there is never a guaranteed analysis. There is only conviction and  believe, but never certainty.
    </p>
    <br>


    <p>

        Candlestick patterns are specific formations created by one or more candlesticks.
        These patterns help traders predict possible market movements.
    </p>

    <p>
        Candlestick patterns are generally divided into:
    </p>

    <ol class="list-inside indent-4 list-[lower-roman]">
        <li>Reversal patterns</li>
        <li>Continuation patterns</li>
    </ol>
    <br>

    <h3> <b>1. Reversal patterns</b></h3>

    <p class="pl-4">
        These patterns suggest that the current trend may change direction.

    Bullish reversal patterns suggest a possible move upward after a downtrend.
    <br>
    Bearish reversal patterns suggest a possible move downward after an uptrend.
</p>
    <br>

    <h3 class=""> <b>2. Continuation patterns </b></h3>

    <p>
        These patterns suggest that the current trend is likely to continue.
    
        Bullish continuation patterns suggest buyers may continue pushing prices upward.Bearish continuation 
        patterns suggest sellers may continue pushing prices downward.
    </p>
    <br>

    <h2>Market psychology behind candlesticks</h2>

    <p>
        Candlesticks reflect the emotional battle between buyers and sellers, some basic pattern translations are shown below.
    </p>

    <ol class="list-inside list-disc indent-4">
        <li>Strong bullish candles show aggressive buying confidence.</li>
        <li>Strong bearish candles show aggressive selling pressure.</li>
        <li>Long upper wicks may indicate rejection from higher prices.</li>
        <li>Long lower wicks may indicate rejection from lower prices.</li>
        <li>Small bodies often show indecision in the market.</li>
    </ol>

    <p>
        Understanding this psychology helps traders interpret what market participants are likely doing.
    </p>
    <br>


    <h2>Limitations of candlesticks</h2>

    <p>
        Candlesticks are useful, but they are not perfect prediction tools.
        They should not be used alone because:
    </p>

    <ol class="list-inside list-disc indent-4">
        <li>False signals can occur</li>
        <li>Market conditions can change suddenly</li>
        <li>Patterns may fail</li>
        <li>Different timeframes may show conflicting information</li>
    </ol>

    <p>
        Professional traders often combine candlestick analysis with:
    </p>

    <ol class="list-inside list-[lower-roman] indent-4">
        <li>Support and resistance</li>
        <li>Trend analysis</li>
        <li>Volume</li>
        <li>Indicators</li>
    </ol>
