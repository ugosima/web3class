    <h2>Candlestick timeframes</h2>

    <p>
        Candlesticks can represent different durations depending on the selected timeframe.
        <br>
        Common timeframes includes 1 minute, 5 minutes, 15 minutes, 1 hour, 4 hours, 1 day, and 1 week.
        <br>
        Smaller timeframes show faster market movements and more noise, while larger timeframes provide a broader and generally more reliable view of market direction.
    </p>
