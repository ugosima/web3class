    <p>
        Trading candlesticks are graphical representations of price movement within a specific period of time in a financial market.
        They are one of the most important tools used in technical analysis because they help traders quickly understand market behavior,
        buyer and seller strength, momentum, and possible future price direction.
    </p>
    <br>

    <x-imageviewer filename="candlestick.png" alt="Candlestick" />
    <p>
        <b>*** Candlesticks ***</b>
    </p>
    <br>

    <p>
        A candlestick is formed from four important price points during a trading period:
    </p>

    <ol class=" list-[lower-roman] list-inside pl-6 marker:font-bold">
        <li>Opening price</li>
        <li>Closing price</li>
        <li>Highest price</li>
        <li>Lowest price</li>
    </ol>

    <p>
        Each candlestick represents a specific time frame depending on the chart being used. For example, on a 5-minute chart,
        one candlestick represents 5 minutes of market activity. On a daily chart, one candlestick represents one full trading day.
    </p>
    <br><br>



    <h2>Structure of a candlestick</h2>

    <p>
        A candlestick has two main parts:
    </p>

    <h3 class="indent-4">   <b>The Body</b> </h3>

    <p>
        The body is the thick middle section of the candlestick. It shows the difference between the opening price and the closing price.
        <br>
        A long body shows strong buying or selling pressure, while a short body shows weaker market movement.
    </p>

    <h3 class="indent-4"> <b>The Wick or Shadow </b></h3>

    <p>
        The thin lines above and below the body are called wicks or shadows.
        <br>
        The upper wick shows the highest price reached during the trading period.The lower wick shows the lowest price reached during the trading period.
    </p>

    <p>
        Wicks reveal price rejection and market volatility.
    </p>
    <br>




    <h2>Types of candlesticks</h2>
    <div class="pl-4">
    <h3> <b>1. </b>Bullish Candlestick</h3>

    <p>
        A bullish candlestick forms when buyers push the closing price above the opening price.
    </p>

    <p>
        This indicates buying pressure and suggests that buyers controlled the market during that period.
    </p>
    

    <p>Example:</p>

    <ul class="list-inside indent-4">
        <li>Open price = $100</li>
        <li>Close price = $110</li>
    </ul>

    <p>
        The candlestick closes higher than it opened, creating a bullish candle, which is usually displayed by a green candlestick.
    </p>

    <h3> <b>2. </b>Bearish candlestick</h3>
    
    <p>
        A bearish candlestick forms when sellers push the closing price below the opening price.
    </p>

    <p>
        This indicates selling pressure and suggests that sellers controlled the market during that period.
    </p>

    <p>Example:</p>

    <ul class="list-inside indent-4">
        <li>Open price = $110</li>
        <li>Close price = $100</li>
    </ul>
    <p>
        The candlestick closes lower than it opened, creating a bearish candle, which is usually displayed by a red candlsestick.
    </p>
    </div>
