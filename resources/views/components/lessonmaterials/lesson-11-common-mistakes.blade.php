     <h2 class="qbheading"><b class="ideabulb">&#128161; </b> <b class="qbheading"> PHISHING SCAM </b> </h2>
    <br>
    <p>
      Phishing is a common tactic used by scammers to trick users into revealing sensitive information, such as private keys or seed phrases.
    </p>
      <br>
    <p>
      It involves scammers creating fake versions of popular websites and deceiving users into entering their credentials or wallet information on fraudulent platforms. For example, "<b>https://metamask.io</b>" is the official website of MetaMask, but a phishing site might look like "<b>https://metamask-secure.com</b>" or "<b>https://metamask-login.com</b>". These fake sites are designed to look exactly like the original, but they exist only to steal your information.
    </p>
      <br>
      <p>
          Always verify URLs, avoid clicking on suspicious links, and never share your private keys or seed phrases with anyone.
      </p>
      <br>
      <p>If you receive an email claiming to be from your wallet provider and asking you to "verify your account" by entering your seed phrase, it is most likely a phishing attempt. Legitimate services will never ask for your seed phrase.</p>
