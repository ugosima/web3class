<div>
    <p>
        The internet has evolved from static web pages to social media, cloud apps, and smart platforms.
        Now, a new phase called Web3 is changing how people think about ownership, privacy, and control online.
    </p>
    <br>

        <p>
            Web3, short for Web 3.0, is the next generation of the internet. Its goal is to reduce dependence on big
            technology companies and give users more control over their data, identity, and digital assets.
            <br>
        </p>

           <p> The internet we use today, often called web2, is built in a way where most of what we do online is controlled by big platforms  like Google, Facebook, Amazon, and 
            other large platforms. These companies provide useful services, but they also control much of the data, identity, and activity that flows through their systems. When you create an account, post content, or build an audience, you are operating inside systems owned by these companies. They store your data, decide how it is used, and can sometimes restrict or remove your access.
            </p>
            <br>
            <p>
            Web3 starts with a simple question. what if this control did not belong to companies but to the user instead?
            <br><br>

            Instead of creating accounts on platforms, Web3 ties your identity to something you fully control, a digital wallet such as MetaMask. This wallet is more than a login tool; it becomes your identity across the internet, anything connected to it belongs to you, not to a platform.

            From there, your activities and assets are recorded on decentralized networks like Ethereum instead of being stored inside a company’s private database, this removes the need for a single organization to control your presence or your assets online.
            <br>
            <br>

             Because of this structure, something important changes; You are no longer just a user on someone else’s platform, you become the owner of your digital identity. the content you create, the assets you hold, and the identity you build are not easily taken away or deleted by a central authority (blocking of accounts).
            <br><br>

            <p>
            This shift also introduces new possibilities. instead of platforms being the only ones that benefit from user activity, web3 allows users to participate in value creation directly. in some systems, people can earn tokens or rewards for contributing, interacting, or building within an ecosystem. Access is also more open, since anyone with an internet connection can participate even without a traditional bank account.
            </p>
    <br>

    <p>
        Simply put, Web3 is a more decentralized version of the internet built with cryptography, blockchain technology,and decentralized protocols that allows users to interact, own digital assets, manage cryptocurrencies, and use
        online services without relying entirely on a central authority.
    </p>
</div>

