<h2>Real-life examples of web3 in action</h2>

<p>
To understand web3 better, it helps to see how it is already being used in simple, practical ways.
</p>

<h3>1. Digital money you control yourself</h3>
<p>
With wallets like <strong>MetaMask</strong>, people can store cryptocurrencies and digital assets without needing a bank. 
This means you can send, receive, or hold money directly, without asking permission from any institution.
</p>

<h3>2. Trading without a middleman</h3>
<p>
Platforms like <strong>Uniswap</strong> allow users to swap cryptocurrencies directly with each other. 
There is no bank or centralized exchange holding your funds or approving your transactions. Everything happens through smart contracts.
</p>

<h3>3. Owning digital items online</h3>
<p>
Marketplaces like <strong>OpenSea</strong> let users buy, sell, and create digital items such as art or collectibles. 
These items are stored on the blockchain, which means ownership can be proven and transferred without a central platform controlling it.
</p>

<h3>4. Storing files without a central company</h3>
<p>
Projects like <strong>Filecoin</strong>, <strong>Arweave</strong>, and <strong>Storj</strong> allow users to store data across many computers instead of one company’s servers. 
This makes storage more distributed and harder to shut down.
</p>

<h3>5. Automatic agreements without lawyers or middlemen</h3>
<p>
Smart contracts are programs that run automatically when conditions are met. 
For example, a payment can be released automatically once a service is delivered, without needing a bank or intermediary to approve it.
</p>

<h3>6. Proving who you are online</h3>
<p>
Decentralized identity systems allow users to create and manage digital identities without relying on a single company like Google or Facebook. 
This means you can log in, verify yourself, or access services while keeping more control over your personal data.
</p>

<p>
    These examples show how Web3 can be used across finance, storage, identity, software, gaming, and digital ownership.
</p>

