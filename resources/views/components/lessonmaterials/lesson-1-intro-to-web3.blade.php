<h2>A quick history</h2>
<p>
    To understand Web3, it helps to look at what came before it:
</p>

<ul>
    <li>
        <b>Web1 (1990s-early 2000s)</b>: Commonly called the "read-only" internet. Users could visit websites, but
        they had little ability to interact with content.
    </li>
    <li>
        <b>Web2 (2000s-now)</b>: The "read-and-write" internet. This era brought social media, cloud apps, and
        e-commerce, but it also gave companies control over large amounts of user data.
    </li>
    <li>
        <b>Web3</b>: The "read-write-own" internet. This version focuses on user control, decentralization, and
        digital ownership.
    </li>
</ul>

<br><br>

<h2>Key features of Web3</h2>

<ol class="pl-3 space-y-2 list-decimal">
    <li>
        <b>Decentralization:</b>
        No single company or entity controls the entire network. Instead, the network runs through many independent
        computers, often called nodes, around the world.
    </li>

    <li>
        <b>Decentralized Applications (dApps):</b>
        Web3 apps run on peer-to-peer networks like blockchains instead of centralized servers. This reduces the control
        any single company has over the app or its user data.
    </li>

    <li>
        <b>Digital Ownership:</b>
        Web3 allows users to own digital items such as art, music, game items, and collectibles through tools like NFTs
        (Non-Fungible Tokens). These assets are managed through wallets controlled by the owner.
    </li>

    <li>
        <b>Built-in Money System:</b>
        Web3 uses cryptocurrencies like Bitcoin and Ethereum to send value without banks, borders, or traditional
        middlemen. This is one of the major reasons Web3 has attracted investors, builders, and crypto users.
    </li>

    <li>
        <b>Smart Contracts:</b>
        Smart contracts are programs stored on a blockchain that run automatically when certain conditions are met.
        For example, a smart contract can release payment after a job is completed.
    </li>

    <li>
        <b>Open Source Code:</b>
        Many Web3 projects are open source, meaning anyone can view, use, or improve the code. This supports
        collaboration and community-driven innovation.
    </li>

    <li>
        <b>Interoperability:</b>
        Web3 apps can work together. For example, you can use one crypto wallet to buy an NFT on one platform and use
        it in another app or game.
    </li>

    <li>
        <b>Permissionless Access:</b>
        Anyone can join and use many Web3 services without approval from a central authority.
    </li>

    <li>
        <b>Transparency:</b>
        Blockchain transactions are usually visible to everyone, making it possible to verify activity without relying
        only on a company or third party.
    </li>

    <li>
        <b>Community Governance:</b>
        Some Web3 projects are governed by their users through decentralized autonomous organizations (DAOs), where
        decisions are made collectively instead of by one company.
    </li>

    <li>
        <b>Privacy and Identity:</b>
        Web3 can use wallets as digital identities, giving users more control over what information they share.
    </li>
</ol>

