    <li><strong>Transaction is Added to a Block:</strong> Once verified, the transaction is grouped with others into a block.</li>
    <li><strong>Block is Added to the Chain:</strong> The block is attached to the existing blockchain in a permanent, unchangeable way.</li>
  </ol>

  <p class="pl-3">Once a block is added, the data it contains is nearly impossible to alter without changing every subsequent block—a feature that makes blockchain highly secure.</p>


