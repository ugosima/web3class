                <p class="pl-3">This order executes immediately at the best available price. it is very useful for quick trades, but may experience price slippage.</p>
            </div>

            <div class=" p-4 rounded-lg shadow mb-4">
                <h3 class="text-lg font-semibold ">2. Limit Order</h3>
                <p class="pl-3">This order lets you set the price and quantity at which you want to buy or sell. The order remains on the book until your set price is attained and your order is matched.</p>
