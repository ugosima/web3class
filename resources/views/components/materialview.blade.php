            <h2 class="text-xl font-semibold  mb-2">What is an Order?</h2>
            <p>An <strong>order</strong> is an instruction you give to the exchange to buy or sell an asset at a specific price and quantity. All trades on centralized exchanges (CEXs) begin with placing an order, which goes into an <strong>order book</strong> where buyers and sellers are matched.</p>
        </section>
<br>
        <section>
            <h2 class="text-xl font-semibold mb-2">Types of Orders</h2>

            <div class="p-4 rounded-lg shadow mb-4">
                <h3 class="text-lg font-semibold ">1. Market Order</h3>
                <p class="pl-3">This order executes immediately at the best available price. it is very useful for quick trades, but may experience price slippage.</p>
            </div>

            <div class=" p-4 rounded-lg shadow mb-4">
                <h3 class="text-lg font-semibold ">2. Limit Order</h3>
                <p class="pl-3">This order lets you set the price and quantity at which you want to buy or sell. The order remains on the book until your set price is attained and your order is matched.</p>
            </div>

            <div class=" p-4 rounded-lg shadow mb-4">
                <h3 class="text-lg font-semibold ">3. Stop Order (Stop-Loss)</h3>
                <p class="pl-3">This order triggers a market order when the asset hits a specific price. it is widely used to reduce losses or enter trends.</p>
            </div>

            <div class="p-4 rounded-lg shadow">
                <h3 class="text-lg font-semibold ">4. Stop-Limit Order</h3>
                <p class="pl-3">Triggers a limit order instead of a market order once the stop price is hit. Offers more control but might not be filled.</p>
            </div>
        </section>
<br>


        <section class="mb-6">
            <h2 class="text-xl font-semibold mb-2">Order duration</h2>
            <p class="pl-3"> Order duration is simply the lifetime of an order, timed conditions determining if an order will be executed, cancelled or expired. They are classified as :</p>

            <ol class="list-[lower-roman] list-inside pl-4">
                <li><strong>GTC (Good Till Cancelled):</strong> Stays active until filled or manually canceled.</li>
                <li><strong>IOC (Immediate or Cancel):</strong> Fills what it can instantly, and cancels the rest.</li>
                <li><strong>FOK (Fill or Kill):</strong> Must fill the entire order immediately or cancel it all.</li>
            </ol>
            <br>
            <p class="pl-3">
              <b class="text-red-300">NOTE </b> : Orders are auto set to GTC by default, meaning they will remain open until filled or canceled. You can change this setting in your exchange account preferences.
            </p>
        </section>
        <br><br>

