  <h2><b>1. Whitepaper</b></h2>
  <p>
    Bitcoin as the first cryptocurrency was introduced by Satoshi Nakamoto in 2008 through the whitepaper titled
    <strong>"Bitcoin: A Peer-to-Peer Electronic Cash System."</strong> It outlined a decentralized network that enables direct
    financial transactions between parties without relying on trusted intermediaries. By solving the double-spending problem
    through a consensus mechanism called <em>Proof-of-Work</em>, the whitepaper laid the foundation for a trustless digital
    payment system. It introduced concepts like distributed ledgers (blockchains), mining, and incentivized validation that are now
    standard across the cryptocurrency ecosystem.
  </p>
