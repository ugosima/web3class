      <h2 class="text-2xl font-bold" >Trading/Currency pairs</h2>

    <p class="pl-3"><strong>Trading pairs</strong> are combinations of two different currencies that can be traded for each other on an exchange. They represent the price relationship between two assets, typically one <strong>base currency</strong> and one <strong>quote currency</strong> ,  symbolized as <b>BASE/QUOTE</b>. For example, if you see the trading pair <strong>BTC/USDT</strong>, this means: you are buying or selling <strong>Bitcoin (BTC)</strong> using <strong>Tether (USDT)</strong>.</p>

    <p class="pl-3">
      In (<b>ETH/BTC</b>) :
      <ol  class="pl-3">
          <li><strong>ETH</strong> is the <strong>base currency</strong> i.e the one you want to buy or sell.</li>
          <li><strong>BTC</strong> is the <strong>quote currency</strong> i.e  the one you use to pay or receive.</li>
      </ol>
      <span class="pl-3">
        If <b>ETH/BTC</b> = 0.07, it means that 1 <b>ETH</b> = <b>0.07 BTC</b>, so you need 0.07 <b>BTC</b> to buy 1 <b>ETH</b>, and if you sell 1 <b>ETH</b>, you will get 0.07 <b>BTC</b>.
      </span>
    </p>
    <br>

    <div>
      <span>
        Trading pairs matters because :
      </span>

      <ol class="pl-3 list-disc space-y-2 list-inside">
        <li><strong>They determine what you can trade</strong>
            <p>Exchanges don’t let you trade any coin for any coin directly. You can only trade coins that have a <strong>direct pair</strong> listed.</p>
        </li>
        <li><strong>They affect your route</strong>
            <p>Say you have USDT, and you want to buy a small token like <b> $XYZ </b>, but the exchange lists <b>$XYZ/BNB</b>, you will need to → buy <b>BNB</b> with <b>USDT</b> → then buy <b>$XYZ</b> with <b>BNB</b>.
                <br>
                This process is called a <strong>trading path</strong>.</p>
        </li>
        <li><strong>They define the price you see</strong>
            <p>You will see coin prices based on the quote currency.</p>
            <ul>
                <li> <b> BTC/USDT</b> = $29,000; Means 1 <b>BTC</b> costs 29,000 <b>USDT</b>.</li>
                <li><b>ETH/BTC</b> = $0.07; Means 1 <b>ETH</b> costs 0.07 <b>BTC</b>.</li>
            </ul>
        </li>
    </ol>
  </div>
    <br>
       
    <p><strong>NOTE : </strong> Many stablecoins (like USDT or BUSD) are <strong>quote currencies</strong> because they offer a price tied to Fiat (dollar).</p>

