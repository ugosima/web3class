  <h2><b>2. Tokenomics</b></h2>
  <p>
    Bitcoin's economic model is deflationary, designed to create scarcity and long-term value preservation. It operates on a fixed
    set of rules embedded in its protocol. These rules determine how new coins are created, how transactions are validated, and how incentives are distributed to participants in the network. 
  </p>
  <p>
    The key components of Bitcoin's tokenomics include : 
  </p>
  <ul>
    <li><strong>Maximum Supply:</strong> BTC has a hard coded total supply of 21 million BTC.</li>
    <li><strong>Block Time:</strong> This is the average amount of time it takes the network to validate and add a new block of transactions to the blockchain, which for BTC is approximately 10 minutes.</li>
    <li><strong>Reward Halving:</strong>The amount of BTC given to miners in halved, this simply means that supply of new bitcoins into the market is halved, and for BTC, this happens at every 210,000 blocks (4 years); current reward is 3.125 BTC.</li>
    <li><strong>Mining Incentives:</strong>Miners are rewarded through the combination of block rewards and transaction fees.</li>
    <li><strong>Deflationary Nature:</strong> Reduced issuance over time; All BTC is expected to be mined by 2140</li>
  </ul>

