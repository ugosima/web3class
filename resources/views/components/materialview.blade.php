  <p>Cryptocurrency can seem like a foreign language when you're just starting out. From blockchains and wallets to gas fees and HODL, the crypto world is full of unique terms of which understanding them  is essential for anyone looking to get involved with cryptocurrency, whether as a user, investor, developer, or enthusiast.</p>

  <p>Let's look at some of the most common and important terminologies in cryptocurrency, ball explained in simple terms.</p>
  <br>

  <b> 1. Blockchain/Network </b> 
  <p>A blockchain is a digital ledger that records all transactions across a network of computers. Each group of transactions forms a "block," and these blocks are linked together in a chain. It is the foundational technology behind cryptocurrencies, every blockchain has it's coin.</p>
<br>

  <b> 2. Coin or Token </b>
  <p>A Coin or Token is any digital form of money that is decentralised and built on blockchain technology. Examples include Bitcoin, Ethereum, and Solana. Coins are made specifically for the blockchain, while tokens are crypto built on an already existing blockchain. </p>
  <p><strong>Note:</strong>Though they are not the same, Token/Coin are used interchangeably to mean the same thing in practise.</p>
<br>

   <b>3. Wallet</b>
  <p>A wallet is a software or hardware tool used to store, send, and receive cryptocurrencies, it is similar to a bank account. It contains your public and private keys, and your wallet address all generated from your seed phrase.</p>
  <ol class="list-[lower-roman] list-inside  indent-4 pl-4">
    <li><strong>Hot Wallet:</strong> Connected to the internet, e.g. Metamask, Trustwallet, Phantom e.t.c.</li>
    <li><strong>Cold Wallet:</strong> Offline, more secure e.g., hardware wallets like Ledger, they are usually specialized devices for crypto transactions.</li>
  </ol>
  <br>

  
  <b>4. Seed phrase / Recovery Phrase</b>
  <p>This is the 12 or 24 words phrase that gives access to a wallets private key, everybody that has this words in the right order will access the funds in the wallet, so it is meant to be protected and must not be revealed to 2nd party;
     it is recommended that they are handwritten on paper and filed up safely, losing them also means losing access to your account. </p>
<br>

  
  <b>5. Private Key / Public Key</b>
  <p>Private and public keys are unique cryptographic keys used in blockchain transactions, they are generated from seed phrases and are uniquely bound to a particular seed phrase. They are essential for securing your wallet and authorizing transactions.</p>
  <p>Never share your private key with anyone, as it gives full control over your wallet and funds. If someone has your private key, they can access and transfer your cryptocurrency without your permission.</p>
  <p>Simply put : </p>
  <ol class="list-[lower-roman] list-inside  indent-4 pl-4">
    <li><strong>Public Key: </strong> Shared openly, and can be used at the backend of dapps to generate your wallet address.</li>
    <li><strong>Private Key: </strong> Kept secret and used to authorize transactions. Losing or exposing this means losing the sole access and ownership to your funds.</li>
  </ol>
  <strong>Note : </strong>A public key is not a wallet address, it is a longer string of characters that is used to generate the wallet address; do not transfer funds to a public key.</p>
  <br>

  <b> 6. Wallet Address </b>
    <p>A crypto address is a unique string of characters used to send or receive cryptocurrency, similar to a bank account number.</p>
      <p>It is derived from your public key and can be shared with others to receive funds; Always double-check the address before sending crypto, as transactions are irreversible, to prevent mistakes, always copy and paste addresses, do not type them.</p>
  <p><strong> Example : </strong> A Bitcoin address looks like this: <code> <b>1A1zP1eP5QGefi2DMPTfTL5SLmv7DivfNa</b></code></p>
  <p><strong>Note : </strong> Always ensure you are using the correct address format for the specific cryptocurrency you are dealing with, as different coins have different address formats; you will develop this knowledge with time.</p>
<br>

 <b>7. Gas Fee </b>
  <p>A gas fee is a small payment required to perform a transaction or execute a smart contract on a blockchain. It pays the network validators for processing.</p>
<br>

  <b>8. Gas Limit </b>
  <p>The gas limit is the maximum amount of gas you are willing to spend on a transaction. If the transaction requires more gas than the limit, it will fail.</p>
<br>

<b>9. DApp (decentralised Application)</b>
  <p>A DApp is an application that runs on a decentralised network, typically using smart contracts. DApps are open-source, operate autonomously, and are not controlled by a single entity.</p>  
  <br>

  <b>10. Faucet</b>
  <p>A faucet is a website or app that gives out small amounts of cryptocurrency for free, usually to help users learn how to use wallets and transactions. It is often used to distribute testnet coins for developers and testers.</p>
<br>

 <b>11. Testnet </b>
  <p>A testnet is a separate blockchain used for testing purposes. It allows developers to experiment with new features without risking real money. Testnets use test coins that have no real value.</p>
